          <h2>Types of Tests</h2>
          <p class="lead"><strong>Antigen Test:</strong></p>
          <p class="lead">Antigen tests are rapid diagnostic tests used to detect the presence of specific viral proteins (antigens) on the surface of the SARS-CoV-2 virus.
The test is usually conducted using a nasal or throat swab sample.
Antigen tests are generally faster than RT-PCR tests and can provide results within 15 to 30 minutes.
These tests are useful for quickly identifying individuals with a high viral load and who are likely to be contagious.
However, they may have a higher chance of false negatives, especially during the early stages of infection or when the viral load is low.</p>
<p class="lead"><strong>RT-PCR (Reverse Transcription Polymerase Chain Reaction) Test:</strong></p>
<p class="lead">RT-PCR is considered the gold standard for COVID-19 testing.
It detects the presence of the genetic material (RNA) of the SARS-CoV-2 virus in a person's respiratory sample (usually collected through a nasal or throat swab).
The test involves a two-step process: first, the viral RNA is converted to DNA (reverse transcription), and then the DNA is amplified (using polymerase chain reaction) to detect the virus's presence.
RT-PCR tests are highly accurate and sensitive, capable of detecting the virus even in individuals with low viral loads.
The turnaround time for RT-PCR test results can vary, but it generally takes a few hours to a few days, depending on the testing capacity and laboratory workload.</p>
<p class="lead"><strong>CB-NAAT (Cartridge-Based Nucleic Acid Amplification Test) Test:<strong></p>
<p class="lead">The CB-NAAT test is a molecular diagnostic test used to detect the presence of the SARS-CoV-2 virus's genetic material.
It is similar to RT-PCR in that it uses nucleic acid amplification techniques to detect the virus.
The CB-NAAT test is designed for use in settings with limited laboratory infrastructure, as it comes in a cartridge-based format and can be performed in relatively simple instruments.
Like RT-PCR, the CB-NAAT test provides accurate results and can detect the virus even in individuals with low viral loads.
The turnaround time for CB-NAAT test results can be relatively quick, typically within a few hours.</p>
